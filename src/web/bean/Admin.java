package web.bean;

public class Admin {
	public String admin1;
	public String admin2;
	
	public Admin() {
		super();
	}
	
	public Admin(String admin1, String admin2) {
		super();
		this.admin1 = admin1;
		this.admin2 = admin2;
	}
	
	public String getAdmin1() {
		return admin1;
	}
	public void setAdmin1(String admin1) {
		this.admin1 = admin1;
	}
	public String getAdmin2() {
		return admin2;
	}
	public void setAdmin2(String admin2) {
		this.admin2 = admin2;
	}
	

	
	
}
